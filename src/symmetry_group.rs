use num_traits::PrimInt;
use rand::RngExt;
use rand::rngs::ThreadRng;
use crate::algebra::Matrix;


pub type SymmetryGroup<T : PrimInt, const D : usize, const N : usize> = [Matrix<T,D,D>; N];

pub trait SymmetryGroupTrait<T : PrimInt, const D : usize> {
	fn random(&self, rng : &mut ThreadRng) -> Matrix<T,D,D>;
}

impl<T : PrimInt, const D : usize, const N : usize> SymmetryGroupTrait<T,D> for SymmetryGroup<T,D,N> {
	fn random(&self, rng : &mut ThreadRng) -> Matrix<T,D,D> {
		self[rng.random_range(0..self.len())]
	}
}



